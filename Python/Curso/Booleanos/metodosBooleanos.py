cadena = "Estoy mostrando los metodos booleanos para las strings"

print(cadena.startswith("e")) # verifica si la cadena inicia con un caracter determinado
print(cadena.endswith("S")) # verifica si la cadena finaliza con un caracter determinado
print(cadena.isupper()) # verifica si la cadena esta en mayuscula
print(cadena.islower()) # verifica si la cadena esta en minuscula
